export function t(lang: Lang, key: string, vars?: Record<string, string>): string {
  let str = T[lang]?.[key] ?? T['uk'][key] ?? key
  if (vars) {
    Object.entries(vars).forEach(([k, v]) => { str = str.replace(`{${k}}`, v) })
  }
  return str
}
